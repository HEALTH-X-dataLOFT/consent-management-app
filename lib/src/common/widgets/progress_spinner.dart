library;

import 'package:flutter/material.dart';

class ProgressSpinner extends StatefulWidget {
  const ProgressSpinner({super.key});

  @override
  ProgressSpinnerState createState() => ProgressSpinnerState();
}

class ProgressSpinnerState extends State<ProgressSpinner>
    with TickerProviderStateMixin {
  late AnimationController _controller;

  @override
  void initState() {
    _controller = AnimationController(
      vsync: this,
      duration: const Duration(seconds: 2),
    )..addListener(() {
        setState(() {});
      });
    _controller.repeat();
    super.initState();
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return CircularProgressIndicator(value: _controller.value);
  }
}
